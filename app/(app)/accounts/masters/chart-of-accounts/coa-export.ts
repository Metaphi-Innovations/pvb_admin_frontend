export interface CoaLedgerExportMeta {
  ledger: ChartOfAccount;
  parentGroup: string;
  dateFrom: string;
  dateTo: string;
  openingAmount: number;
  openingSide: "Debit" | "Credit";
  closingAmount: number;
  closingSide: "Debit" | "Credit";
}

function formatLedgerRowDate(iso: string): string {
  return iso ? isoToDisplayDate(iso) : "—";
}

export async function exportCoaLedgerStatementToExcel(
  rows: CoaLedgerDetailRow[],
  meta: CoaLedgerExportMeta,
): Promise<void> {
  const XLSX = await import("xlsx");

  const dataRows = rows.map((r) => ({
    Date: formatLedgerRowDate(r.date),
    "Voucher Type": r.voucherType,
    "Voucher No.": r.voucherNo,
    Particulars: r.isOpeningRow ? "Opening Balance" : r.partyName || r.narration || "—",
    "Debit (₹)": r.debit || "",
    "Credit (₹)": r.credit || "",
    Balance: r.runningBalance > 0 ? formatMoney(r.runningBalance) : "—",
    "Dr/Cr":
      r.runningBalance > 0
        ? balanceSideLabel(
            resolveDrCrColumnSide({
              debit: r.debit,
              credit: r.credit,
              runningBalanceType: r.runningBalanceType,
              isBalanceRow: Boolean(r.isOpeningRow),
            }),
          )
        : "—",
    Narration: r.narration || "—",
  }));

  const wb = XLSX.utils.book_new();
  XLSX.utils.book_append_sheet(wb, XLSX.utils.json_to_sheet(dataRows), "Ledger Statement");

  const info = XLSX.utils.aoa_to_sheet([
    ["Company Name", COMPANY_NAME],
    ["Report Name", "Ledger Statement"],
    ["Ledger Name", meta.ledger.accountName],
    ["Ledger Code", meta.ledger.accountCode],
    ["Parent Group", meta.parentGroup],
    ["Date Range", `${meta.dateFrom} to ${meta.dateTo}`],
    ["Opening Balance", formatBalanceAmount(meta.openingAmount, meta.openingSide)],
    ["Closing Balance", formatBalanceAmount(meta.closingAmount, meta.closingSide)],
    ["Generated By", ACCOUNTS_CURRENT_USER],
    ["Generated On", generatedOnLabel()],
  ]);
  XLSX.utils.book_append_sheet(wb, info, "Report Info");

  const safeName = meta.ledger.accountName.replace(/[^\w]+/g, "_").slice(0, 40);
  const date = new Date().toISOString().slice(0, 10);
  XLSX.writeFile(wb, `Ledger_${safeName}_${date}.xlsx`);
}

