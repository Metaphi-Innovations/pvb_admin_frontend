const COMPANY_NAME = "Dharitri Sutra Agri Solutions Pvt Ltd";
const REPORT_NAME = "Chart of Accounts";

export interface CoaExportMeta {
  dateFrom: string;
  dateTo: string;
}

function generatedOnLabel(): string {
  return new Date().toLocaleString("en-IN", {
    dateStyle: "medium",
    timeStyle: "short",
  });
}

function formatBalance(amount: number, side: "Debit" | "Credit"): string {
  if (amount <= 0) return "—";
  return `${amount.toLocaleString("en-IN")} ${side === "Debit" ? "Dr" : "Cr"}`;
}

export interface CoaLedgerListingExportMeta {
  groupName: string;
}

export async function exportCoaLedgerListingToExcel(
  rows: CoaLedgerListingRow[],
  meta: CoaLedgerListingExportMeta,
): Promise<void> {
  const XLSX = await import("xlsx");

  const dataRows = rows.map((r) => ({
    "Ledger Name": r.ledger.accountName,
    "Ledger Code": r.ledger.accountCode,
    "Parent Group": r.parentGroupName,
    Source: r.source,
    "Opening Balance": formatBalance(r.openingAmount, r.openingSide),
    "Current Balance": formatBalance(r.currentAmount, r.currentSide),
    Status: r.ledger.status === "active" ? "Active" : "Inactive",
  }));

  const wb = XLSX.utils.book_new();
  XLSX.utils.book_append_sheet(wb, XLSX.utils.json_to_sheet(dataRows), "Ledgers");

  const info = XLSX.utils.aoa_to_sheet([
    ["Company Name", COMPANY_NAME],
    ["Report Name", "Chart of Accounts — Ledger Listing"],
    ["Accounting Group", meta.groupName],
    ["Generated By", ACCOUNTS_CURRENT_USER],
    ["Generated On", generatedOnLabel()],
  ]);
  XLSX.utils.book_append_sheet(wb, info, "Report Info");

  const safeGroup = meta.groupName.replace(/[^\w]+/g, "_").slice(0, 40);
  const date = new Date().toISOString().slice(0, 10);
  XLSX.writeFile(wb, `COA_Ledgers_${safeGroup}_${date}.xlsx`);
}

