export function buildReportDocumentHtml(options: ReportDocumentOptions): string {
  const css = buildReportPrintCss({
    landscape: options.landscape,
    compact: options.compact,
  });
  const headerHtml = buildReportHeaderHtml(options.header);
  const footerNote = options.footerHtml ?? "";

  return `<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="utf-8" />
  <title>${escapeHtml(options.title)}</title>
  <style>${css}</style>
</head>
<body>
  ${headerHtml}
  ${options.bodyHtml}
  ${footerNote}
  <script>window.onload = function() { window.print(); };</script>
</body>
</html>`;
}

export function buildReportExcelDocumentHtml(options: ReportDocumentOptions): string {
  const headerHtml = buildReportHeaderHtml(options.header);
  const footerNote = options.footerHtml ?? "";
  const sheetName = options.header.reportTitle.slice(0, 31).replace(/[\\/*?:[\]]/g, "");

  return `<!DOCTYPE html>
<html xmlns:o="urn:schemas-microsoft-com:office:office" xmlns:x="urn:schemas-microsoft-com:office:excel">
<head>
  <meta charset="utf-8" />
  <!--[if gte mso 9]><xml><x:ExcelWorkbook><x:ExcelWorksheets><x:ExcelWorksheet><x:Name>${escapeHtml(sheetName)}</x:Name></x:ExcelWorksheet></x:ExcelWorksheets></x:ExcelWorkbook></xml><![endif]-->
  <style>${buildReportExcelCss()}</style>
</head>
<body>
  ${headerHtml}
  ${options.bodyHtml}
  ${footerNote}
</body>
</html>`;
}

export function openReportPrintWindow(html: string): void {
  const win = window.open("", "_blank");
  if (!win) return;
  win.document.write(html);
  win.document.close();
}

export function downloadReportExcelHtml(html: string, filename: string): void {
  const blob = new Blob([html], { type: "application/vnd.ms-excel;charset=utf-8;" });
  const url = URL.createObjectURL(blob);
  const anchor = document.createElement("a");
  anchor.href = url;
  anchor.download = filename.endsWith(".xls") ? filename : `${filename}.xls`;
  anchor.click();
  URL.revokeObjectURL(url);
}

export function exportReportToPdf(options: ReportDocumentOptions): void {
  openReportPrintWindow(buildReportDocumentHtml(options));
}

export function exportReportToExcelHtml(options: ReportDocumentOptions, filename: string): void {
  downloadReportExcelHtml(buildReportExcelDocumentHtml(options), filename);
}

export function todayExportDateSuffix(): string {
  return new Date().toISOString().slice(0, 10);
}

/** Build a standard tabular PDF from headers + row HTML. */
export function exportTabularReportToPdf(options: {
  title: string;
  header: ReportHeaderOptions;
  columns: ReportColumnHeader[];
  bodyHtml: string;
  footerHtml?: string;
  footerNote?: string;
  landscape?: boolean;
}): void {
  const tableHtml = buildStandardReportTableHtml({
    columns: options.columns,
    bodyHtml: options.bodyHtml,
    footerHtml: options.footerHtml,
  });
  exportReportToPdf({
    title: options.title,
    header: options.header,
    bodyHtml: tableHtml,
    footerHtml: options.footerNote ? `<p class="report-footer-note">${options.footerNote}</p>` : undefined,
    landscape: options.landscape,
  });
}

/** Build a standard tabular Excel HTML export matching the PDF layout. */
export function exportTabularReportToExcelHtml(options: {
  title: string;
  header: ReportHeaderOptions;
  columns: ReportColumnHeader[];
  bodyHtml: string;
  footerHtml?: string;
  footerNote?: string;
  filename: string;
}): void {
  const tableHtml = buildStandardReportTableHtml({
    columns: options.columns,
    bodyHtml: options.bodyHtml,
    footerHtml: options.footerHtml,
  });
  exportReportToExcelHtml(
    {
      title: options.title,
      header: options.header,
      bodyHtml: tableHtml,
      footerHtml: options.footerNote ? `<p class="report-footer-note">${options.footerNote}</p>` : undefined,
    },
    options.filename,
  );
}
