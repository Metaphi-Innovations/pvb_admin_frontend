import { ACCOUNTS_CURRENT_USER } from "@/lib/accounts/config";
import { formatMoney, formatMoneyNumber, formatMoneyOrDash } from "@/lib/accounts/money-format";

/** Standard company name for all accounting report exports. */
export const ACCOUNTS_COMPANY_NAME = "Dharitri Sutra Agri Solutions Pvt Ltd";

/** Accounting hierarchy row types used across financial statement exports. */
export type HierarchyRowType =
  | "title"
  | "primary"
  | "group"
  | "subgroup"
  | "header"
  | "section"
  | "line"
  | "subtotal"
  | "total"
  | "net"
  | "summary";

export interface ReportFilterLine {
  label: string;
  value: string;
}

export interface ReportHeaderOptions {
  reportTitle: string;
  financialYear?: string;
  /** Pre-formatted period label, e.g. "01 Apr 2024 to 31 Mar 2025" or "As on 31 Mar 2025". */
  reportPeriod?: string;
  dateFrom?: string;
  dateTo?: string;
  asOnDate?: string;
  subtitle?: string;
  filters?: ReportFilterLine[];
}

export interface ReportColumnHeader {
  label: string;
  align?: "left" | "right" | "center";
  className?: string;
}

export interface TAccountExportRow {
  rowType: HierarchyRowType | "title";
  leftParticular: string;
  leftIndent: number;
  leftAmount: number | null;
  leftBold?: boolean;
  rightParticular: string;
  rightIndent: number;
  rightAmount: number | null;
  rightBold?: boolean;
}

export interface ReportDocumentOptions {
  title: string;
  header: ReportHeaderOptions;
  bodyHtml: string;
  footerHtml?: string;
  landscape?: boolean;
  compact?: boolean;
}

const INDENT_SPACES = 4;

export function generatedOnLabel(): string {
  return new Date().toLocaleString("en-IN", {
    dateStyle: "medium",
    timeStyle: "short",
  });
}

export function escapeHtml(value: string): string {
  return value
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

/** Indent label for accounting hierarchy (Primary → Group → Sub Group → Ledger). */
export function indentParticular(label: string, indent: number, spacesPerLevel = INDENT_SPACES): string {
  if (!label) return "";
  return `${" ".repeat(spacesPerLevel * Math.max(0, indent))}${label}`;
}

export function formatExportAmount(
  amount: number | null | undefined,
  options?: { isReturn?: boolean; dashWhenZero?: boolean; rawNumber?: boolean },
): string {
  if (amount == null) return "";
  if (options?.dashWhenZero && amount === 0) return "—";
  if (options?.rawNumber) return formatMoneyNumber(amount);
  if (options?.isReturn && amount > 0) return `(${formatMoney(amount)})`;
  if (amount < 0) return `(${formatMoney(Math.abs(amount))})`;
  return formatMoney(amount);
}

export function formatExportAmountOrDash(amount: number): string {
  return formatMoneyOrDash(amount);
}

export function hierarchyRowClass(rowType: HierarchyRowType | "title"): string {
  return rowType;
}

export function resolveReportPeriod(header: ReportHeaderOptions): string {
  if (header.reportPeriod) return header.reportPeriod;
  if (header.asOnDate) return `As on ${header.asOnDate}`;
  if (header.dateFrom && header.dateTo) return `${header.dateFrom} to ${header.dateTo}`;
  return "";
}

export function buildMetaRows(header: ReportHeaderOptions, extraRows: string[][] = []): string[][] {
  const rows: string[][] = [
    ["Company Name", ACCOUNTS_COMPANY_NAME],
    ["Report Name", header.reportTitle],
  ];
  const period = resolveReportPeriod(header);
  if (header.financialYear) rows.push(["Financial Year", header.financialYear]);
  if (period) rows.push(["Report Period", period]);
  if (header.subtitle) rows.push(["Subtitle", header.subtitle]);
  for (const filter of header.filters ?? []) {
    rows.push([filter.label, filter.value]);
  }
  rows.push(["Generated By", ACCOUNTS_CURRENT_USER]);
  rows.push(["Generated On", generatedOnLabel()]);
  return [...rows, ...extraRows];
}

function buildFilterLinesHtml(header: ReportHeaderOptions): string {
  const lines: string[] = [];
  if (header.subtitle) {
    lines.push(`<p class="report-subtitle">${escapeHtml(header.subtitle)}</p>`);
  }
  for (const filter of header.filters ?? []) {
    lines.push(
      `<p class="report-filter"><span class="filter-label">${escapeHtml(filter.label)}:</span> ${escapeHtml(filter.value)}</p>`,
    );
  }
  lines.push(
    `<p class="report-generated">Generated by ${escapeHtml(ACCOUNTS_CURRENT_USER)} · ${escapeHtml(generatedOnLabel())}</p>`,
  );
  return lines.join("\n");
}

/** Centered report header — company name, title, FY, and period. */
export function buildReportHeaderHtml(header: ReportHeaderOptions): string {
  const period = resolveReportPeriod(header);
  const fyLine = header.financialYear
    ? `<p class="report-fy"><strong>Financial Year:</strong> ${escapeHtml(header.financialYear)}</p>`
    : "";
  const periodLine = period
    ? `<p class="report-period"><strong>Report Period:</strong> ${escapeHtml(period)}</p>`
    : "";

  return `<header class="report-header">
  <p class="report-company">${escapeHtml(ACCOUNTS_COMPANY_NAME)}</p>
  <h1 class="report-title">${escapeHtml(header.reportTitle)}</h1>
  ${fyLine}
  ${periodLine}
  ${buildFilterLinesHtml(header)}
</header>`;
}

export function buildBalanceMessageHtml(
  message: string,
  variant: "balanced" | "unbalanced" | "info" | "warning",
): string {
  return `<p class="balance-msg ${variant}">${escapeHtml(message)}</p>`;
}

export function buildStandardReportTableHtml(options: {
  columns: ReportColumnHeader[];
  bodyHtml: string;
  footerHtml?: string;
  tableClass?: string;
}): string {
  const headCells = options.columns
    .map((col) => {
      const align = col.align ?? "left";
      const cls = [align !== "left" ? align : "", col.className ?? ""].filter(Boolean).join(" ");
      return `<th class="${cls}">${escapeHtml(col.label)}</th>`;
    })
    .join("");

  const tfoot = options.footerHtml ? `<tfoot>${options.footerHtml}</tfoot>` : "";
  const tableClass = options.tableClass ? ` class="${options.tableClass}"` : "";

  return `<table${tableClass}>
  <thead><tr>${headCells}</tr></thead>
  <tbody>${options.bodyHtml}</tbody>
  ${tfoot}
</table>`;
}

export function buildTAccountTableHtml(options: {
  leftTitle: string;
  leftAmountHeader: string;
  rightTitle: string;
  rightAmountHeader: string;
  rowsHtml: string;
}): string {
  return `<table class="t-account">
  <thead>
    <tr>
      <th class="label-col">${escapeHtml(options.leftTitle)}</th>
      <th class="num">${escapeHtml(options.leftAmountHeader)}</th>
      <th class="divider" aria-hidden="true"></th>
      <th class="label-col">${escapeHtml(options.rightTitle)}</th>
      <th class="num">${escapeHtml(options.rightAmountHeader)}</th>
    </tr>
  </thead>
  <tbody>${options.rowsHtml}</tbody>
</table>`;
}

export function buildTAccountRowHtml(row: TAccountExportRow): string {
  const rowClass = hierarchyRowClass(row.rowType);
  const leftLabel = escapeHtml(indentParticular(row.leftParticular, row.leftIndent));
  const rightLabel = escapeHtml(indentParticular(row.rightParticular, row.rightIndent));
  const leftBold = row.leftBold ? " bold" : "";
  const rightBold = row.rightBold ? " bold" : "";
  const leftAmount = formatExportAmount(row.leftAmount);
  const rightAmount = formatExportAmount(row.rightAmount);

  if (row.rowType === "title") {
    return `<tr class="${rowClass}">
      <td class="label bold" colspan="2">${leftLabel}</td>
      <td class="divider"></td>
      <td class="label" colspan="2"></td>
    </tr>`;
  }

  return `<tr class="${rowClass}">
    <td class="label${leftBold}">${leftLabel}</td>
    <td class="num${leftBold}">${leftAmount}</td>
    <td class="divider"></td>
    <td class="label${rightBold}">${rightLabel}</td>
    <td class="num${rightBold}">${rightAmount}</td>
  </tr>`;
}

